 class TV {
    private String marca;
    private int polegadas;

     TV(String marca, int polegadas) {
        this.marca = marca;
        this.polegadas = polegadas;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getPolegadas() {
        return polegadas;
    }

    public void setPolegadas(int polegadas) {
        this.polegadas = polegadas;
    }
}

// Classe principal para testar
 class PrincipalTV {
    public static void main(String[] args) {
        TV tv = new TV("Samsung", 55);
        System.out.println("Marca: " + tv.getMarca());
        System.out.println("Polegadas: " + tv.getPolegadas());

        tv.setMarca("LG");
        tv.setPolegadas(65);
        System.out.println("Nova marca: " + tv.getMarca());
        System.out.println("Novas polegadas: " + tv.getPolegadas());
    }
}